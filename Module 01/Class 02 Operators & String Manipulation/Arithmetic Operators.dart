void main() {
  int a = 10;
  int b = 20;

  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  double Division = a / b;
  int integerDivision = a ~/ b;
  int remainder = a % b;

  print("Sum: $sum");
  print("Difference: $difference");
  print("Product: $product");
  print("Division: $Division");
  print("Integer Division: $integerDivision");
  print("Remainder: $remainder");

  int x = 5;
  x++; // Increment
  print("Incremented x: $x");

  x--; // Decrement
  print("Decremented x: $x");
}
